package soru_Cozumleri;

public class ForLoop_Soru_3 {
    public static void main(String[] args) {
        // 100 den 50 ye kadar-50 dahil- sayilari ayni
        // satirda aralrinda virgulle yazma

        for (int i = 100; i >=50 ; i--) {
            if(i>50){
                System.out.print(i+",");
            }
            else System.out.println(i);

        }
    }
}
